var t = true, f = false;

// AND 연산
console.log(t && t);
console.log(t && f);
console.log(f && t);
console.log(f && f);

console.log("===============================");
// OR 연산
console.log(t || t);
console.log(t || f);
console.log(f || t);
console.log(f || f);

console.log("===============================");
// NOT 연산
console.log(!true);
console.log(!false);
